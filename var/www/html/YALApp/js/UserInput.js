// a UserInput object
function UserInput(first, // a string
	last, // a string
	password, // a string
	email, // a string
	phoneNumber, // a string
	hireDate, // a string
	department	// a string
	)
{
	this.first = first;
	this.last = last;
	this.password = password; 
	this.email = email;
	this.phoneNumber = phoneNumber;
	this.hireDate = hireDate;
	this.department = department;
}